#include <bits/stdc++.h>
using namespace std;

void solve_me_first(int a, int b) {
    cout << a + b << "\n";
}

int main() {
    // Making IO operations faster
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int a, b;
    cin >> a >> b;
    solve_me_first(a, b);

    return 0;
}